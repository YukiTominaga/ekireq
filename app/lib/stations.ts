export function stationKey(prefecture: string, stationName: string): string {
  return `${prefecture}:${stationName}`;
}

export function getAllStations(): StationWithMeta[] {
  const result: StationWithMeta[] = [];
  for (const pref of PREFECTURES) {
    const lines = STATION_DATA[pref];
    for (const [line, stations] of Object.entries(lines)) {
      for (const stn of stations) {
        result.push({
          ...stn,
          prefecture: pref,
          line,
          key: stationKey(pref, stn.name),
        });
      }
    }
  }
  return result;
}

export function getUniqueStations(): StationWithMeta[] {
  const seen = new Set<string>();
  const result: StationWithMeta[] = [];
  for (const stn of getAllStations()) {
    if (!seen.has(stn.key)) {
      seen.add(stn.key);
      result.push(stn);
    }
  }
  return result;
}
